char	*ft_strlowcase(char *str);

char	*ft_strlowcase(char *str)
{
	unsigned long long	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'A') && (str[i] <= 'Z'))
		{
			str[i] += 32;
		}
		i++;
	}
	return (str);
}
