int	ft_plus(int a, int b)
{
	return (a + b);
}

int	ft_minus(int a, int b)
{
	return (a - b);
}

int	ft_divide(int a, int b)
{
	return (a / b);
}

int	ft_multiply(int a, int b)
{
	return (a * b);
}

int	ft_modulo(int a, int b)
{
	return (a % b);
}
